    private void refresh() {
        DefaultTableModel modeloTabla = new DefaultTableModel();
        modeloTabla.addColumn("ID");
        modeloTabla.addColumn("MARCA");
        modeloTabla.addColumn("DESCRIPCION");
        modeloTabla.addColumn("PRECIO");
        modeloTabla.addColumn("COLOR");
        modeloTabla.addColumn("FRACUENCIA");
        modeloTabla.addColumn("TAMAÑO");
        listaCelulares = celularesControl.listar();
        Object[] fila = new Object[7];
        for (int i = 0; i < listaCelulares.size(); i++) {
            celularesObj = listaCelulares.get(i);
            fila[0] = celularesObj.getId();
            fila[1] = celularesObj.getMarcasdescr();
            fila[2] = celularesObj.getDescripcion();
            fila[3] = celularesObj.getPrecio();
            fila[4] = celularesObj.getColor();
            fila[5] = celularesObj.getFrecuencia();
            fila[6] = celularesObj.getTamanho();
            modeloTabla.addRow(fila);

        }

        tblCelulares.setModel(modeloTabla);

        DefaultComboBoxModel modeloComboBox = new DefaultComboBoxModel();
        listaMarcas = marcasControl.listar();
        modeloComboBox.addElement("***********");

        for (int i = 0; i < listaMarcas.size(); i++) {
            modeloComboBox.addElement(listaMarcas.get(i).getNombre());
        }
        cbxMarcas.setModel(modeloComboBox);

        id = null;
        cbxMarcas.setSelectedIndex(0);
        txtDescripcion.setText("");
        txtPrecio.setText("");
        txtColor.setText("");
        txtFrecuencia.setText("");
        txtTamanho.setText("");

    }

