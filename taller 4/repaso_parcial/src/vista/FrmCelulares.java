    private void btnGrabarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGrabarActionPerformed
        Integer opc = cbxMarcas.getSelectedIndex();
        if (opc == 0) {
            JOptionPane.showMessageDialog(null, "debe seleccionar un registro!!");
        } else {
            try {
                opc--;
                String descripcion = txtDescripcion.getText();
                Integer precio = Integer.parseInt(txtPrecio.getText());
                String color = txtColor.getText();
                Integer frecuencia = Integer.parseInt(txtFrecuencia.getText());
                String tamanho = txtTamanho.getText();
                Integer marcasId = listaMarcas.get(opc).getId();
                celularesObj = new Celulares(
                        id,
                        marcasId,
                        null,
                        descripcion,
                        precio,
                        color,
                        frecuencia,
                        tamanho
                );

                if (id == null) {
                    celularesControl.insertar(celularesObj);
                } else {
                    celularesControl.modificar(celularesObj);
                }
                refresh();

            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(null, "precio y frecuencia deben ser numericos!");
            }

        }


    }//GEN-LAST:event_btnGrabarActionPerformed

    private void jmiModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jmiModificarActionPerformed

        Integer fila = tblCelulares.getSelectedRow();
        if (fila < 0) {
            JOptionPane.showMessageDialog(null, "seleccione un registro");

        } else {
            id = (Integer) tblCelulares.getValueAt(fila, 0);
            cbxMarcas.setSelectedItem(tblCelulares.getValueAt(fila, 1));
            txtDescripcion.setText(tblCelulares.getValueAt(fila, 2).toString());
            txtPrecio.setText(tblCelulares.getValueAt(fila, 3).toString());
            txtColor.setText(tblCelulares.getValueAt(fila, 4).toString());
            txtFrecuencia.setText(tblCelulares.getValueAt(fila, 5).toString());
            txtTamanho.setText(tblCelulares.getValueAt(fila, 6).toString());

        }

    }//GEN-LAST:event_jmiModificarActionPerformed

    private void jmiEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jmiEliminarActionPerformed
        Integer fila = tblCelulares.getSelectedRow();
        if (fila < 0) {
            JOptionPane.showMessageDialog(null, "seleccione un registro");
        } else {
            Integer opc = JOptionPane.showConfirmDialog(null, "estas seguro???");
            if (opc == 0) {
                id = Integer.parseInt(tblCelulares.getValueAt(fila, 0).toString());
                celularesObj = new Celulares(id);
                celularesControl.eliminar(celularesObj);
                refresh();
            }

        }


    }//GEN-LAST:event_jmiEliminarActionPerformed

